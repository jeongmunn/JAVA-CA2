// add button 
    private void addbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addbuttonActionPerformed
        String nametoadd = "", organizertoadd = "", datentimetoadd = "", feestoaddt = "" , typeofevent = "";
        Double feestoadd = 0.0;
        boolean error = false;

        // event name
        if ((name.getText()).isEmpty()) {
            JOptionPane.showMessageDialog(null, "EVENT NAME cannot be null ! PLease key in again.", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            nametoadd = name.getText();

            // event organizer
            if ((organizer.getText()).isEmpty()) {
                JOptionPane.showMessageDialog(null, "EVENT ORGANIZER cannot be null ! Please key in again.", "Error", JOptionPane.ERROR_MESSAGE);
            } else {
                organizertoadd = organizer.getText();

                // event date & time
                if ((datentime.getText()).isEmpty()) {
                    JOptionPane.showMessageDialog(null, "EVENT DATE & TIME cannot be null ! Please key in again.", "Error", JOptionPane.ERROR_MESSAGE);
                } else {
                    datentimetoadd = datentime.getText();

                    // event fees 
                    if ((fees.getText()).isEmpty()) {
                        JOptionPane.showMessageDialog(null, "EVENT FEES cannot be null ! Please key in again.", "Error", JOptionPane.ERROR_MESSAGE);
                    } else {
                        feestoaddt = fees.getText();

                        try {
                            feestoadd = Double.parseDouble(feestoaddt);
                        } catch (NumberFormatException e) {
                            JOptionPane.showMessageDialog(null, "EVENT FEES is not a number ! Please key in again.", "Error", JOptionPane.ERROR_MESSAGE);
                            error = true;
                        }

                        if (online.isSelected() || facetoface.isSelected()) {
                            if(online.isSelected()){
                                typeofevent = "Online Event" ;
                            }else if(facetoface.isSelected()){
                                typeofevent = "Face-to-Face Event" ;
                            }
                            if (error == false) {
                                try {
                                    // add event
                                    event.Add(nametoadd, organizertoadd, datentimetoadd, feestoadd,typeofevent);
                                    // display result
                                    resultdisplay.setText(event.Display());
                                } catch (Exception e) {
                                    JOptionPane.showMessageDialog(null, "Unknown error while adding and display the new event. Please try again.", "Error", JOptionPane.ERROR_MESSAGE);
                                }
                            }
                        } else {
                            JOptionPane.showMessageDialog(null, "Please indicate the event is online or face-to-face.", "Error", JOptionPane.ERROR_MESSAGE);
                        }
                    }

                }
            }
        }
    }//GEN-LAST:event_addbuttonActionPerformed

