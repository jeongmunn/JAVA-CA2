// search button by "name"   
    private void searchbynamebuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchbynamebuttonActionPerformed
        // check if event name input is empty
        if ((nametosearch.getText()).isEmpty()) {
            JOptionPane.showMessageDialog(null, "EVENT NAME cannot be null ! Please key in again.", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            String searchname = nametosearch.getText();
            try {
                resultdisplay.setText(event.Search1(searchname));
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Unknown error while displaying search result. Please try again.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_searchbynamebuttonActionPerformed

